#include <mc32p21.h>
#include "externVar.H"
#include "CONST.H"


void int_isr(void) __interrupt
{
	//;键盘中断---------------------------------------------------
	if (KBIF) KBIF = 0;
	//;定时器1中断子程序------------------------------------------
	if (T1IE&&T1IF)
		{
		T1IF = 0;
		F1ms = 1;
		}
	//;定时器1中断子程序------------------------------------------
	if (T0IE&&T0IF)
		{
		T0IF = 0;
		F1ms = 1;
		}
	//;外部中断1子程序------------------------------------------
	if (INT1IE&&INT1IF) INT1IF = 0;
}	
	
